package com.examplenestedif;

public class IfExample2 {

	public static void main(String[] args) {
		int x = -50;
		if (x > 0) {
			System.out.println("X is Greater Than Zero");
		} else {
			System.out.println("X is Less Than Zero");
		}
		System.out.println("-----------------------------");
		
		
		String name = "Sourav";
		if (name.equals("Sourav")) {
			System.out.println("Two Strings are Same");
		} else {
			System.out.println("Two Strings are not Same");
		}
	}

}
