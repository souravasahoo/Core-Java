package com.practicestring;

public class ExampleString8 {

	public static void main(String[] args) {
		String s="My Name is Sourav";
		System.out.println(s.indexOf('N'));
		
		System.out.println(s.indexOf("is"));
		
		System.out.println(s.lastIndexOf('a'));
		
		System.out.println(s.charAt(5));
		
		System.out.println(s.contains("Name"));
		
		System.out.println(s.startsWith("S"));
		
		System.out.println(s.endsWith("Sourav"));

	}

}
