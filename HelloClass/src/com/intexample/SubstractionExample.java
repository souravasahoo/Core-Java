package com.intexample;

public class SubstractionExample {

	int num1 = 877;
	int num2 = 98;

	public void sub() {
		int result = num1 - num2;
		System.out.println("Substraction Result : " + result);

	}

	public static void main(String[] args) {
		SubstractionExample a = new SubstractionExample();
		a.sub();
	}

}
