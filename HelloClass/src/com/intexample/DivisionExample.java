package com.intexample;

public class DivisionExample {
	int num1 = 7787;
	int num2 = 67;

	public void div() {
		int result = num1 / num2;
		System.out.println("Division of Two Number :" + result);

	}

	public static void main(String[] args) {
		DivisionExample a = new DivisionExample();
		a.div();

	}
}
