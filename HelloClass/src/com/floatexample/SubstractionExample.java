package com.floatexample;

public class SubstractionExample {
	float a = 67876.668f;
	float b = 5746.67895f;

	public void sub() {
		float c = a - b;
		System.out.println("Substraction Results: " + c);
	}

	public static void main(String[] args) {
		SubstractionExample result = new SubstractionExample();
		result.sub();

	}

}
